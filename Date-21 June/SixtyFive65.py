#Tuple
# Tuple are immutable lists, meaning, values of the elements within the tuple cannot be changed.
#  Tuple is declared with parentheses, ()
#  Tuples are useful when you want to return more than one value from a function.

#Example usage
t = tuple([10, 20, 30, 40])
print(t)
print(type(t))

