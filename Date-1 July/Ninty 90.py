# Method Overloading:
# In this, more than one method of the same class shares the same method name having different signatures.
# Python does not support method overloading directly. But we can achieve this using optional parameters or args.
# Method Overriding:
# In this, the derived class overrides the method of the base class.

class mathUtils:
    def add(self, a, b):
        return a + b

    def sub(self, a, b, c):
        return a - b - c

